public class ProductVO {
	
	private String productno = "";
	
	private String showNo = "";
	
	private String sampleDesc = "";
	
	private String showSampleNo = "";
	
	private String drawPos = "";
	
	private String showDrawNo = "";
	
	private String resultKind = "";

	public String getProductno() {
		return productno;
	}

	public void setProductno(String productno) {
		this.productno = productno;
	}

	public String getShowNo() {
		return showNo;
	}

	public void setShowNo(String showNo) {
		this.showNo = showNo;
	}

	public String getSampleDesc() {
		return sampleDesc;
	}

	public void setSampleDesc(String sampleDesc) {
		this.sampleDesc = sampleDesc;
	}

	public String getShowSampleNo() {
		return showSampleNo;
	}

	public void setShowSampleNo(String showSampleNo) {
		this.showSampleNo = showSampleNo;
	}

	public String getDrawPos() {
		return drawPos;
	}

	public void setDrawPos(String drawPos) {
		this.drawPos = drawPos;
	}

	public String getShowDrawNo() {
		return showDrawNo;
	}

	public void setShowDrawNo(String showDrawNo) {
		this.showDrawNo = showDrawNo;
	}

	public String getResultKind() {
		return resultKind;
	}

	public void setResultKind(String resultKind) {
		this.resultKind = resultKind;
	}

}
