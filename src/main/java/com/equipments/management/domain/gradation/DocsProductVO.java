public class DocsProductVO {
	
	private String docsproductid = "";
	
	private String productno = "";
	
	private String docsfilename = "";
	
	private String selected = "";

	public String getDocsproductid() {
		return docsproductid;
	}

	public void setDocsproductid(String docsproductid) {
		this.docsproductid = docsproductid;
	}

	public String getProductno() {
		return productno;
	}

	public void setProductno(String productno) {
		this.productno = productno;
	}

	public String getDocsfilename() {
		return docsfilename;
	}

	public void setDocsfilename(String docsfilename) {
		this.docsfilename = docsfilename;
	}

	public String getSelected() {
		return selected;
	}

	public void setSelected(String selected) {
		this.selected = selected;
	}

}
